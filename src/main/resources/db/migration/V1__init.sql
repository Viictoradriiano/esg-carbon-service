-- Flyway V1__init.sql
-- Creates basic tables for ESG_APP schema.

CREATE TABLE COMPANY (
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  NAME VARCHAR2(200) NOT NULL,
  CNPJ VARCHAR2(32) UNIQUE,
  SECTOR VARCHAR2(100)
);

CREATE TABLE EMISSION_RECORD (
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  COMPANY_ID NUMBER NOT NULL,
  SCOPE VARCHAR2(16) NOT NULL,
  DATE_AT DATE NOT NULL,
  AMOUNT_TCO2E NUMBER(18,3) DEFAULT 0 NOT NULL,
  SOURCE VARCHAR2(100),
  NOTES VARCHAR2(500),
  CONSTRAINT FK_EMISSION_COMPANY FOREIGN KEY (COMPANY_ID) REFERENCES COMPANY(ID)
);

CREATE TABLE OFFSET_INITIATIVE (
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  COMPANY_ID NUMBER NOT NULL,
  TYPE VARCHAR2(32) NOT NULL,
  START_DATE DATE,
  END_DATE DATE,
  EST_OFFSET_TCO2E NUMBER(18,3) DEFAULT 0 NOT NULL,
  STATUS VARCHAR2(16) DEFAULT 'PLANNED',
  CONSTRAINT FK_OFFSET_COMPANY FOREIGN KEY (COMPANY_ID) REFERENCES COMPANY(ID)
);

CREATE TABLE MITIGATION_SCHEDULE (
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  COMPANY_ID NUMBER NOT NULL,
  ACTION_DESC VARCHAR2(200) NOT NULL,
  SCHEDULED_DATE DATE,
  EST_REDUCTION_TCO2E NUMBER(18,3),
  STATUS VARCHAR2(16) DEFAULT 'PLANNED',
  CONSTRAINT FK_MITIGATION_COMPANY FOREIGN KEY (COMPANY_ID) REFERENCES COMPANY(ID)
);

